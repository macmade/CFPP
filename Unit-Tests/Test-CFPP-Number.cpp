static bool FloatIsEqual( CF::Number::Float32 x, CF::Number::Float32 y );
static bool FloatIsEqual( CF::Number::Float64 x, CF::Number::Float64 y );

static bool FloatIsEqual( CF::Number::Float32 x, CF::Number::Float32 y )
{
    return fabsf( x - y ) < FLT_EPSILON;
}

static bool FloatIsEqual( CF::Number::Float64 x, CF::Number::Float64 y )
{
    return fabs( x - y ) < DBL_EPSILON;
}

